SELECT NOME_DO_PRODUTO, PRECO_DE_LISTA, 
CASE 
	WHEN PRECO_DE_LISTA >= 12 THEN 'CARO' 
	WHEN PRECO_DE_LISTA >= 7 AND PRECO_DE_LISTA < 12 THEN 'EM CONTA' 
	ELSE 'BARATO' 
END AS STATUS_PRECO 
FROM TABELA_DE_PRODUTOS;

